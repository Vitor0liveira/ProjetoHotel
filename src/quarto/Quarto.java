package quarto;

public class Quarto {

    private int nr_quarto;
    private String ds_quarto;
    private int andar;
    private String tipo;
    private float preco;

    public int getNr_quarto() {
        return nr_quarto;
    }

    public void setNr_quarto(int nr_quarto) {
        this.nr_quarto = nr_quarto;
    }

    public String getDs_quarto() {
        return ds_quarto;
    }

    public void setDs_quarto(String ds_quarto) {
        this.ds_quarto = ds_quarto;
    }

    public int getAndar() {
        return andar;
    }

    public void setAndar(int andar) {
        this.andar = andar;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

}
